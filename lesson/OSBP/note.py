
#star for loop

for i in range(1,6):
    print('*'*i)

#star for loop in reverse order

for i in range(5,0,-1):
    print('*'*i)

#star for loop in reverse order with space

for i in range(5,0,-1):
    print(' '*(5-i)+'*'*i)

#star for loop in order with space 

for i in range(1,6):
    print(' '*(5-i)+'*'*i)

